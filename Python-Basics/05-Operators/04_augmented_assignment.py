"""
------------------------------------------------------------
Problem No : 04
Topic      : Operators
Problem    : Augmented Assignment
Difficulty : Easy

Question:
Start with a variable value = 10 and demonstrate +=, -=, *=, /=
by updating and printing the value after each operation.

Example Output:
Initial value: 10
After += 5: 15
After -= 3: 12
After *= 2: 24
After /= 4: 6.0
------------------------------------------------------------
"""

# Solution
value = 10
print("Initial value:", value)
value += 5
print("After += 5:", value)
value -= 3
print("After -= 3:", value)
value *= 2
print("After *= 2:", value)
value /= 4
print("After /= 4:", value)
